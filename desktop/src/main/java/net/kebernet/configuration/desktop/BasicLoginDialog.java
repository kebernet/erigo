    public BasicLoginDialog() {
        setContentPane(contentPane);
        setModal(true);
        getRootPane().setDefaultButton(buttonOK);

        buttonOK.addActionListener(e -> onOK());
        buttonCancel.addActionListener((e) -> this.dispose());
    }

    private void onOK() {
        HttpClient.BasicAuthenticationToken token = new HttpClient.BasicAuthenticationToken(userName.getText(), password.getText());
        callback.accept(token);
        dispose();
    }

    public static void main(String[] args) {
        BasicLoginDialog dialog = new BasicLoginDialog();
        dialog.pack();
        dialog.setVisible(true);
        System.exit(0);
    }

    @Override
    public void authenticationRequired(String deviceName, String url, HttpClient.AuthenticationToken previousToken, Consumer<HttpClient.AuthenticationToken> callback) {
        this.deviceName.setText(deviceName);
        this.url.setText(url);
        if (previousToken != null && previousToken instanceof HttpClient.BasicAuthenticationToken) {
            HttpClient.BasicAuthenticationToken token = (HttpClient.BasicAuthenticationToken) previousToken;
            this.userName.setText(token.getUsername());
            this.password.setText(token.getPassword());
        }
        this.callback = callback;
        pack();
        setVisible(true);
    }


